	/** {@inheritDoc} */
	@Override
	public IExpr divide(IExpr that) {
		if (that.isNumber()) {
			return F.eval(F.Times(this, that.inverse()));
		}
		if (that.isOne()) {
			return this;
		}
		if (that.isMinusOne()) {
			return negate();
		}
		return F.eval(F.Times(this, F.Power(that, F.CN1)));
	}

