	@Override
	public boolean equals(final Object obj) {
		if (obj instanceof AbstractAST) {
			if (hashCode() != obj.hashCode()) {
				return false;
			}
			if (obj == this) {
				return true;
			}
			IAST list = (IAST) obj;
			if (size() != list.size()) {
				return false;
			}
			int size = size();
			for (int i = 0; i < size; i++) {
				if (!get(i).equals(list.get(i))) {
					return false;
				}
			}
			return true;
		}
		return false;
	}

