	@Override
	public int hashCode() {
		if (hashValue == 0) {
			hashValue = 17;
			int size = size();
			for (int i = 0; i < size; i++) {
				// http://stackoverflow.com/questions/4948780/magic-number-in-boosthash-combine
				// hashValue ^= array[i].hashCode() + 0x9e3779b9 + (hashValue << 6) + (hashValue >> 2);
				hashValue = 23 * hashValue + get(i).hashCode();
			}
		}
		return hashValue;
	}

