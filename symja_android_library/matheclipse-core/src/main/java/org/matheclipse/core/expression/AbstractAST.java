	/** {@inheritDoc} */
	@Override
	public final IAST filter(IAST filterAST, Predicate<IExpr> predicate, int maxMatches) {
		int count = 0;
		if (count >= maxMatches) {
			return filterAST;
		}
		int size = size();
		for (int i = 1; i < size; i++) {
			if (predicate.apply(get(i))) {
				if (++count == maxMatches) {
					filterAST.add(get(i));
					break;
				}
				filterAST.add(get(i));
			}
		}
		return filterAST;
	}

